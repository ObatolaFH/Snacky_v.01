        {
            for (int y = 0; y < maze.GetLength(1); y++)
            {
                GameObject clone = Instantiate(mazeObjects[maze[x, y]], new Vector3(transform.position.x + (y * factor), transform.position.y - (x * factor), 0), Quaternion.identity);
